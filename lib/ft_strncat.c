#include "libft.h"

void	ft_strncat(char *s1, char *s2, int len1, int len2)
{
	int i;
	int j;

	i = 0;
	j = len1;
	while (i < len2)
	{
		s2[j] = s1[i];
		i++;
		j++;
	}
}
